这批“命运多舛”的文物也吸引了网友们的关注。

一名外国网友在看到这些历经千年，依然“神采奕奕”的青铜器后感叹道：“即使利用今天的工艺，即使我们知道铸造它们的方法，我们还是难以完美复制这些器具。”



