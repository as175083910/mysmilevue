可耻的盗墓走私

这批具有重要学术价值的青铜组器是怎么流失到日本的？

研究表明，这批文物是我国湖北随州地区春秋早期曾国高等级贵族墓葬出土文物，经过锈蚀产物分析与锈蚀层性状的观察等，可判断其为近年来被盗掘出土。

2019年3月3日，国家文物局接到举报，称日本某拍卖公司拟于近期拍卖的曾伯克父青铜组器疑为我国非法流失文物。国家文物局当即展开调查并发现，该批青铜组器曾于2014年在上海出现过。

国家文物局遂向全国21家文物进出境审核管理处查证。“所有管理处均未办理该批青铜组器的临时进出境手续，有力证实该批青铜组器应为2014年之后被非法出口至日本。” 国家文物局副局长关强介绍说。

当机立断的追索

3月6日，国家文物局研究决定立即启动追索工作。3月9日，国家文物局向日本驻华使馆通报流失文物信息，提出曾伯克父青铜组器是非法出口的中国文物并涉嫌为被盗掘走私文物的相关证据，依据中日两国共同加入的联合国教科文组织《关于禁止和防止非法进出口文化财产和非法转让其所有权的方法的公约》规定，提请日方采取一切必要措施开展相关工作，协助中方妥善解决该批青铜组器的返还问题。在外交与刑侦双重压力下，日本拍卖公司最终在其官网发布声明，暂停曾伯克父青铜组器的拍卖。



经文物部门和公安机关多方施加压力，文物持有人、上海居民周某于7月同意将该组青铜器无条件上交国家并配合公安机关调查。8月20日，国家文物局、公安部派出的联合工作组，在我使馆和日本外务省代表见证下，于使馆完成鉴定与接收工作。8月23日深夜，这批文物安全抵京，8月24日凌晨安全入库。

“这非常有意义”

在国际上，如何追索流失海外的文物是许多国家都面临的问题，颇受各国舆论的关注。此次中国在短短数个月内便成功追索了一批珍贵文物，自然吸引了外媒和外国网友的眼球。

新加坡《联合早报》报道称， 曾伯克父器物群品类丰富，铸造精致、保存完整，为目前考古发现所未见，对于研究中国春秋时期历史文化、曾国宗法世系以及青铜器断代与铸造工艺具有重要的学术价值。



