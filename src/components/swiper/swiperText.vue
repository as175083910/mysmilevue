       中国日报网9月11日电 又有一组流失海外的文物回家了！

10日上午，国家文物局召开新闻发布会，宣布流失日本多年的曾伯克父青铜组器已被成功追索，重新回到祖国怀抱。

曾伯克父青铜组器一组8件，包括1鼎、1簋、1甗、1霝、2盨、2壶，均有铭文，共 330字，蕴含着丰富的历史文化信息，被整体鉴定为国家一级文物。



