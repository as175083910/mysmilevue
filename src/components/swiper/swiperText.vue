文物回家任重道远

近年来，通过执法合作、外交斡旋、司法诉讼、谈判协商等多种方式，中国成功从英国、美国、法国、丹麦、加拿大、澳大利亚等国追回30余批次近4000件套流失文物。

今年4月10日，796件（套）文物艺术品在漂泊海外多年后终于踏上祖国的土地。而这批珍贵文物回家的背后，是10余年漫长的追索之路。2007年，意大利文化遗产保护宪兵部队在本国文物市场查获一批疑似非法流失的中国文物艺术品，随即启动国内司法审判程序。我国国家文物局得知相关信息后，立即对接意大利文化遗产主管部门，开展流失文物的追索返还工作。2019年年初，意大利法院最终作出向中方返还796件（套）文物艺术品的判决。据悉，这是近20年来最大规模的中国文物艺术品返还，为通过国际合作开展流失文物追索返还树立了新的范例。



4月24日，由文化和旅游部、国家文物局联合主办的“归来——意大利返还中国流失文物展”在中国国家博物馆开幕。图为观众在展柜前欣赏展出的文物。新华社记者 李贺 摄

今年2月28日，美国向中国归还361件（套）流失文物，这批文物时间跨度从新石器时代直至清代，涉及石器、玉器、青铜器、陶器、钱币、木雕建筑构件等多个门类，多为中国古代墓葬随葬器物。这批中国流失文物艺术品于2014年4月由美国联邦调查局印第安纳波利斯分局查获。我国国家文物局随后便依据相关国际公约，向美方提出声索要求，全力推进返还。

尽管已经有诸多成功实践，但流失文物的追索返还仍然任重道远。据中国文物学会统计，1840年鸦片战争以来，超过1000万件中国文物流失到欧美、日本和东南亚等国家和地区。因此还有更多的文物期待“回家”。正如《人民日报》在一篇评论文章中所说，要点亮“国宝”漫漫回家路，需要举各方之力。具体而言，需要进一步拓展文物追索渠道，形成海外文物追索的机制；需要和国际社会更深入地沟通、更紧密地合作，也需要更多社会力量的参与和推动。

